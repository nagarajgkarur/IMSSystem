package com.iamsystem.hive9.response;

public class Users {
	
	private String userId;
	private String firstName;
	private String lastName;
	private String email;
	private String jobTitle;
	private String phone;
	private String preferredCurrencyCode;
	private String preferredDateFormat;
	private String activateDelegatedApprover;
	private String userRole;
	private String roleId;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getJobTitle() {
		return jobTitle;
	}
	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getPreferredCurrencyCode() {
		return preferredCurrencyCode;
	}
	public void setPreferredCurrencyCode(String preferredCurrencyCode) {
		this.preferredCurrencyCode = preferredCurrencyCode;
	}
	public String getPreferredDateFormat() {
		return preferredDateFormat;
	}
	public void setPreferredDateFormat(String preferredDateFormat) {
		this.preferredDateFormat = preferredDateFormat;
	}
	public String getActivateDelegatedApprover() {
		return activateDelegatedApprover;
	}
	public void setActivateDelegatedApprover(String activateDelegatedApprover) {
		this.activateDelegatedApprover = activateDelegatedApprover;
	}
	public String getUserRole() {
		return userRole;
	}
	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}
	public String getRoleId() {
		return roleId;
	}
	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
	
	
}
